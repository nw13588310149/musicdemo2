/// 视频发布者展示数据（前端临时填充，与后端无关）。
///
/// 后端目前没有返回视频作者昵称 / 头像，但产品希望视频卡上能呈现"真人感"
/// 的发布者信息。这里维护两份等长数组：
/// - [_nicknames]：150 个昵称（按用户给定的清单录入，覆盖温柔治愈 / 简约 /
///   清冷 / 古风 / 软萌 / 文艺 6 大风格各 25 个）
/// - 头像由 `assets/images/avtor/{1..150}.jpg` 直接构成，文件名即下标 +1
///
/// 设计原则：
/// 1. **同一视频每次进入页面看到的发布者必须一致**，不能随机跳。所以这里用
///    `videoId` 的稳定 hash 取模来选下标，而不是 `Random()`。
/// 2. 昵称数 / 头像数完全相等（都是 150），但二者**独立 hash**，避免出现
///    "1 号头像永远绑 1 号昵称" 这种有规律的搭配。
/// 3. 任何空 / 异常 id 一律落到第 0 项，保证函数永远返回有效结果。
class VideoPublisherInfo {
  const VideoPublisherInfo({required this.nickname, required this.avatarAsset});

  final String nickname;
  final String avatarAsset;
}

/// 根据视频 id 返回稳定的发布者信息（昵称 + 头像 asset 路径）。
VideoPublisherInfo videoPublisherFor(String videoId) {
  final id = videoId.trim();
  final nickIndex = id.isEmpty ? 0 : _stableHash(id) % _nicknames.length;
  // 头像用一个独立的 salt，避免与昵称的下标完全同步。
  final avatarIndex = id.isEmpty
      ? 0
      : _stableHash('avatar:$id') % _avatarCount;
  return VideoPublisherInfo(
    nickname: _nicknames[nickIndex],
    avatarAsset: 'assets/images/avtor/${avatarIndex + 1}.jpg',
  );
}

/// 头像总数（与 `assets/images/avtor/` 中文件数严格对齐）。
const int _avatarCount = 150;

/// 简易非负 32-bit FNV-1a hash，跨平台稳定（不依赖 hashCode 的 isolate 随机化）。
int _stableHash(String s) {
  int h = 0x811c9dc5;
  for (int i = 0; i < s.length; i++) {
    h = (h ^ s.codeUnitAt(i)) & 0xFFFFFFFF;
    h = (h * 0x01000193) & 0xFFFFFFFF;
  }
  return h;
}

